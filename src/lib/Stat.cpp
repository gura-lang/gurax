namespace Gurax {

//------------------------------------------------------------------------------
// Stat
//------------------------------------------------------------------------------
String Stat::ToString(const StringStyle& ss) const
{
	String str;
	str.Format("%s:%dbytes", GetPathName(), GetBytes());
	return str;
}

//------------------------------------------------------------------------------
// StatList
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// StatOwner
//------------------------------------------------------------------------------
void StatOwner::Clear()
{
	for (Stat* pStat : *this) Stat::Delete(pStat);
	clear();
}

}
